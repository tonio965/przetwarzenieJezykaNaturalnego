package zadanie5;

public class FrequentItemToZad5 {
	int occurences;
	String value;
	
	public FrequentItemToZad5(int occurences, String value) {
		super();
		this.occurences = occurences;
		this.value = value;
	}

	public int getOccurences() {
		return occurences;
	}

	public void setOccurences(int occurences) {
		this.occurences = occurences;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}
	
	
	

}
